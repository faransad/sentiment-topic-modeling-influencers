# Import libraries
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
import re

# ================================
# Load Data
# ================================
videos = pd.read_csv("videos.csv")          # columns: influencer, views, likes, comments
comments = pd.read_csv("comments.csv")      # columns: influencer, clean_text

# ================================
# Emoji Counting Function
# ================================
def count_emojis(text):
    emoji_pattern = re.compile(
        "["
        u"\U0001F600-\U0001F64F"  # emoticons
        u"\U0001F300-\U0001F5FF"  # symbols & pictographs
        u"\U0001F680-\U0001F6FF"  # transport & map
        u"\U0001F1E0-\U0001F1FF"  # flags
        u"\u2600-\u26FF\u2700-\u27BF"
        "]+",
        flags=re.UNICODE
    )
    return len(emoji_pattern.findall(str(text)))

# Apply emoji count
comments["emoji_count"] = comments["clean_text"].apply(count_emojis)

# ================================
# Influencer-Level Aggregation
# ================================

# Video-based metrics
influencer_summary = (
    videos
    .groupby("influencer")
    .agg(
        total_views=("views", "sum"),
        avg_views=("views", "mean"),
        total_likes=("likes", "sum"),
        avg_likes=("likes", "mean"),
        total_comments=("comments", "sum")
    )
    .reset_index()
)

# Comment count per influencer
num_comments = (
    comments
    .groupby("influencer")
    .size()
    .reset_index(name="num_comments")
)

# Emoji usage per influencer
emoji_summary = (
    comments
    .groupby("influencer")["emoji_count"]
    .sum()
    .reset_index()
)

# Merge all summaries
influencer_summary = influencer_summary.merge(num_comments, on="influencer")
influencer_summary = influencer_summary.merge(emoji_summary, on="influencer")

print("=== Influencer Summary ===")
print(influencer_summary)

# ================================
# Visualization Function
# ================================
def hbar_plot(df, x_col, y_col, title, xlabel):
    plt.figure(figsize=(10, 6))
    sns.barplot(data=df, y=y_col, x=x_col, orient="h")

    plt.title(title)
    plt.xlabel(xlabel)
    plt.ylabel("Influencer")

    # Add value labels
    for i, v in enumerate(df[x_col]):
        plt.text(v + max(df[x_col]) * 0.01, i, f"{v:,.0f}", va="center")

    plt.tight_layout()
    plt.show()

# ================================
# Plots
# ================================

hbar_plot(influencer_summary, "total_views", "influencer",
          "Total Views per Influencer", "Views")

hbar_plot(influencer_summary, "avg_views", "influencer",
          "Average Views per Video", "Views")

hbar_plot(influencer_summary, "total_likes", "influencer",
          "Total Likes per Influencer", "Likes")

hbar_plot(influencer_summary, "avg_likes", "influencer",
          "Average Likes per Video", "Likes")

hbar_plot(influencer_summary, "num_comments", "influencer",
          "Total Comments per Influencer", "Number of Comments")

hbar_plot(influencer_summary, "emoji_count", "influencer",
          "Total Emoji Usage per Influencer", "Number of Emojis")

