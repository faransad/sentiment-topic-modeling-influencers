likert_cols = ["Q5. I consider the influencers I follow to be trustworthy.",
               "Q6. Influencers I follow seem authentic rather than scripted.",
               "Q7. I feel a sense of personal connection with influencers I follow.",
               "Q8. I trust influencers' opinions on lifestyle-related topics.",
               "Q9. Influencer content often affects my mood or emotions.",
               "Q10. I feel emotionally motivated by influencer content.",
               "Q11. I relate emotionally to the lifestyles presented by influencers.",
               "Q12. Influencer content has influenced my lifestyle habits (e.g., routines, self-care, fitness).",
               "Q13. I have tried products, services, or routines recommended by influencers.",
               "Q14. Influencer content influences my perception of an “ideal” lifestyle."]

df[likert_cols] = df[likert_cols].apply(pd.to_numeric, errors="coerce")

# ================================
# 3. Construct-Level Aggregation
# ================================
constructs = {
    "Credibility & Trust": ["Q5", "Q6", "Q7", "Q8"],
    "Emotional Impact": ["Q9", "Q10", "Q11"],
    "Lifestyle & Purchase Influence": ["Q12", "Q13", "Q14"]
}

# Summary table
summary = []
for name, questions in constructs.items():
    summary.append({
        "Construct": name,
        "Mean Score": df[questions].mean().mean()
    })
construct_table = pd.DataFrame(summary)
print("=== Construct Mean Scores ===")
print(construct_table)

# ================================
# 4. Correlation Analysis
# ================================
# Compute construct scores per respondent
df["Credibility & Trust"] = df[constructs["Credibility & Trust"]].mean(axis=1)
df["Emotional Impact"] = df[constructs["Emotional Impact"]].mean(axis=1)
df["Lifestyle & Purchase Influence"] = df[constructs["Lifestyle & Purchase Influence"]].mean(axis=1)

# Correlation matrix
construct_cols_for_corr = ["Credibility & Trust", "Emotional Impact", "Lifestyle & Purchase Influence"]
cor_matrix = df[construct_cols_for_corr].corr()
print("\n=== Correlation Matrix ===")
print(cor_matrix)

# Heatmap
sns.heatmap(cor_matrix, annot=True, cmap="coolwarm", vmin=-1, vmax=1)
plt.title("Correlation Matrix of Survey Constructs")
plt.show()

# ================================
# 5. Regression Analysis
# ================================
X = df[["Credibility & Trust", "Emotional Impact"]]  # predictors
y = df["Lifestyle & Purchase Influence"]             # dependent variable
X = sm.add_constant(X)  # add intercept
model = sm.OLS(y, X).fit()
print("\n=== Regression Model Summary ===")
print(model.summary())

# ================================
# 6. Group Comparison by Trust
# ================================
median_trust = df["Credibility & Trust"].median()
df["Trust Group"] = df["Credibility & Trust"].apply(lambda x: "High Trust" if x >= median_trust else "Low Trust")

# Mean per group
group_means = df.groupby("Trust Group")[["Emotional Impact", "Lifestyle & Purchase Influence"]].mean()
print("\n=== Mean Scores by Trust Group ===")
print(group_means)

# T-tests
high = df[df["Trust Group"]=="High Trust"]
low = df[df["Trust Group"]=="Low Trust"]

ttest_emotion = ttest_ind(high["Emotional Impact"], low["Emotional Impact"])
ttest_lifestyle = ttest_ind(high["Lifestyle & Purchase Influence"], low["Lifestyle & Purchase Influence"])

