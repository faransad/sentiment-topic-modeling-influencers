# Import libraries
import pandas as pd
import re
from langdetect import detect
from tqdm import tqdm

# Enable progress bar
tqdm.pandas()

# ================================
# Load Data
# ================================
comments_df = pd.read_csv("comments.csv")

# ================================
# Basic Cleaning
# ================================
comments_df = comments_df.dropna(subset=["comment_text"])
comments_df["comment_text"] = comments_df["comment_text"].astype(str)

# ================================
# Text Cleaning Function
# ================================
def clean_text(text):
    text = text.lower()

    # Remove URLs
    text = re.sub(r"http\S+|www\S+", "", text)

    # Remove mentions
    text = re.sub(r"@\w+", "", text)

    # Normalize whitespace
    text = re.sub(r"\s+", " ", text).strip()

    return text

# Apply cleaning
comments_df["clean_text"] = comments_df["comment_text"].apply(clean_text)

# ================================
# Remove Duplicates
# ================================
comments_df = comments_df.drop_duplicates(subset=["clean_text"])

# ================================
# Remove Very Short Comments
# ================================
comments_df["text_length"] = comments_df["clean_text"].str.len()
comments_df = comments_df[comments_df["text_length"] >= 5]

# ================================
# Language Detection
# ================================
def detect_language(text):
    try:
        return detect(text)
    except:
        return "unknown"

comments_df["language"] = comments_df["clean_text"].progress_apply(detect_language)

# Keep only English comments
comments_df = comments_df[comments_df["language"] == "en"]

# ================================
# Spam/Bot Filtering
# ================================
spam_patterns = [
    "subscribe",
    "check my channel",
    "free followers",
    "giveaway",
    "click here",
    "promo"
]

def is_spam(text):
    return any(pattern in text for pattern in spam_patterns)

comments_df["is_spam"] = comments_df["clean_text"].apply(is_spam)
comments_df = comments_df[comments_df["is_spam"] == False]

# ================================
# Final Cleanup
# ================================
comments_df = comments_df.drop(columns=["text_length", "is_spam"])
comments_df = comments_df.reset_index(drop=True)

# ================================
# Save Cleaned Data
# ================================
comments_df.to_csv("comments_cleaned.csv", index=False)

print("✅ Cleaned comments saved as comments_cleaned.csv")

